/// A utility class that defines standard spacing values for consistent UI layout.
///
/// This class provides named constants for commonly used spacing values.
/// It cannot be instantiated.
class AppSpacing {
  // Private constructor to prevent instantiation.
  AppSpacing._();

  /// No spacing.
  static const none = 0.0;

  /// Extra extra small spacing of 2.0.
  static const xxs = 2.0;

  /// Extra small spacing of 4.0.
  static const xs = 4.0;

  /// Small spacing of 6.0.
  static const sm = 6.0;
}
